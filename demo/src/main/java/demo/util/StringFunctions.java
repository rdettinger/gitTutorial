package demo.util;

public class StringFunctions {

	/**
	 * It seems appropriate that we have a comment that tells you that this will
	 * service as a concatenation function for Java.  Not sure why they didn't 
	 * provide one directly for you.  Sigh.
	 * 
	 * @param str1
	 * @param str2
	 * @return  String The combination of the two input parameters.
	 */
	public String concat(String str1, String str2) {
		// Bring str1 together with str2.   
		return str1 + str2;
	}
}
